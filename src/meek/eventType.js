export const EventType = {}

/**
 * Triggered when map is needed to render
 *
 * @property CHANGE
 * @static
 * @final
 * @type {string}
 */
EventType.CHANGE = 'change'

/**
 * Triggered when a image download error
 *
 * @property ERROR
 * @static
 * @final
 * @type {string}
 */
EventType.ERROR = 'error'

/**
 * Triggered when a image finish load
 *
 * @property LOAD
 * @static
 * @final
 * @type {string}
 */
EventType.LOAD = 'load'


export default {
  EventType
}







